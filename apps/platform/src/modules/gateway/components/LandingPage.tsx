export function LandingPage({ onNavigate }: LandingPageProps = {}) {
  const { user } = useAuth();

  // Filter apps based on user permissions
  const availableApps = APPS.filter(app => user?.permissions?.includes(app.id));

  const handleClick = (path: string) => {
    if (onNavigate) {
      onNavigate(`${path}/`);
    } else {
      window.location.href = `${path}/`;
    }
  };

  return (
    <div className="landing">
      <Hero
        title="Boilerplate"
        subtitle="Plateforme de gestion avec authentification JWT et design system intégré"
      />

      {/* Dashboard: derniers elements + credits + raccourcis */}
      <Dashboard onNavigate={onNavigate} />

      {availableApps.length === 0 ? (
        <div className="landing-no-apps">
          <p>Aucune application disponible.</p>
          <p>Contactez un administrateur pour obtenir les droits d'accès.</p>
        </div>
      ) : (
        <div className="landing-apps-grid">
          {availableApps.map(app => (
            <div
              key={app.id}
              role="button"
              tabIndex={0}
              onClick={() => handleClick(app.path)}
              className="shared-card shared-card--interactive shared-card--clickable landing-app-card"
              style={{ ['--card-color' as string]: app.color, ['--card-color-end' as string]: app.gradientEnd }}
            >
              <div
                className="landing-app-card__icon"
                style={{ background: `linear-gradient(135deg, ${app.color} 0%, ${app.gradientEnd} 100%)` }}
              >
                {ICONS[app.id] || (
                  <svg width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2"><rect x="3" y="3" width="18" height="18" rx="2" /></svg>
                )}
              </div>
              <div className="landing-app-card__content">
                <h3 className="landing-app-card__title">{app.name}</h3>
                <p className="landing-app-card__description">{app.description}</p>
              </div>
              <svg className="landing-app-card__arrow" width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
                <polyline points="9 18 15 12 9 6" />
              </svg>
            </div>
          ))}
        </div>
      )}
    </div>
  );
}
